# 2차원 배열 생성의 함정

N, M = 3, 4

# ---------------------------------------------------------
# 1. [Bad Case] 얕은 복사로 인한 문제 발생
# ---------------------------------------------------------
# 리스트 [0]*M 의 주소값만 N번 복사됨 (모든 행이 같은 주소를 가리킴)
arr_bad = [[0] * M] * N

print('=== Bad Case 초기 상태 ===')
print(arr_bad)

# 첫 번째 행의 첫 번째 원소만 바꿨는데...
arr_bad[0][0] = 1

print('\n=== [0][0] 변경 후 ===')
for row in arr_bad:
    print(row)  # 모든 행의 첫 번째 원소가 1로 변한 것을 확인!


print('\n' + '=' * 30 + '\n')


# ---------------------------------------------------------
# 2. [Good Case] 리스트 컴프리헨션을 이용한 올바른 생성
# ---------------------------------------------------------
# 반복될 때마다 새로운 [0]*M 리스트 객체를 생성함
